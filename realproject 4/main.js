var express = require('express');
var mysql = require('./dbcon.js');
var bodyParser = require('body-parser');

var app = express();
var handlebars = require('express-handlebars').create({defaultLayout:'main'});

app.engine('handlebars', handlebars.engine);
app.use(bodyParser.urlencoded({extended:true}));
app.use('/static', express.static('public'));
app.set('view engine', 'handlebars');
app.set('port', 5545);
app.set('mysql', mysql);
app.use('/weapon_character', require('./weapon_character.js'));  //need change for the next few line
app.use('/character', require('./character.js'));
app.use('/Spaceship', require('./Spaceship.js'));
app.use('/weapon', require('./weapon.js'));
app.use('/planet', require('./planet.js'));
app.use('/one2m', require('./one2m.js'));
app.use('/', express.static('public'));

app.use(function(req,res){
  res.status(404);
  res.render('404');
});

app.use(function(err, req, res, next){
  console.error(err.stack);
  res.status(500);
  res.render('500');
});

app.listen(app.get('port'), function(){
  console.log('Express started on http://localhost:' + app.get('port') + '; press Ctrl-C to terminate.');
});
